                        <p class="hero-text">A “personal injury” or a “personal injury lawyer” simply means that the attorney represents individuals who have been hurt.  As an injured person, what you are required to prove are basically three parts of your case.</p>

                        <p>The first is that the person/company that caused your injuries must have had a “duty” to not cause you injuries.  "Duty" simply means an obligation to do or not do something.   The law says that a person has to "act as an ordinarily prudent person would act under the same or similar circumstances."   In the courtroom, the Judge will call this the "reasonable person test."</p>

                        <p>In most cases, this is actually pretty simple. In car wreck cases, any driver has the duty to stop at red lights and if they drove through it, they are not acting like a reasonable person because they drove through a red light</p>

                        <p>The second is that you have to prove that the other person/company breached their duty to act responsibly.  In our car wreck example, the driver breached their duty to follow the traffic laws by running through the red light.</p>

                        <p>The third is that you have to prove that the person who had the duty, and then breached the duty, caused you “damages.   Think of your damages as the dollars or other type of compensation that the negligent person should pay you for your damages. In most cases this will mean your medical bills, your lost wages, and your physical pain and mental anguish damages. All of these damages must be related to the incident.</p>

                        <p>Dale has been working on personal injury cases for the past 23 years and knows how to prove these three requirements for your case and how to maximize your damages.   Give him a call.</p>


                        
